#!/bin/bash
# AWS 볼륨 확장 후 파일 시스템 확장 스크립트
# 사용법: ./expand_disk.sh

echo "=========================================="
echo "💾 디스크 확장"
echo "=========================================="

echo ""
echo "1️⃣ 현재 디스크 사용량 확인:"
df -h

echo ""
echo "2️⃣ 파티션 확인:"
lsblk

echo ""
echo "3️⃣ 파일 시스템 확장 중..."

# 파티션 확장
echo "   파티션 확장 중..."
sudo growpart /dev/nvme0n1 1

# 파일 시스템 확장
echo "   파일 시스템 확장 중..."
sudo resize2fs /dev/nvme0n1p1

echo ""
echo "4️⃣ 확장 후 디스크 사용량:"
df -h

echo ""
echo "=========================================="
echo "✅ 디스크 확장 완료!"
echo "=========================================="

